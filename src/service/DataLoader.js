export class DataLoader {
  static async getHistoricalAllCountries(numberOfDays) {
    try {
      const countryCodesList = encodeURIComponent(COUNTRY_CODES.join(','));
      const historicalAllCountriesJson = await fetch(`${DISEASESH_BASE}/historical/${countryCodesList}?lastdays=${numberOfDays}`);
      const historicalAllCountries = await historicalAllCountriesJson.json();
      return historicalAllCountries;
    } catch (err) {
      throw new Error('Failed to load historicalAllCountries');
    }
  }

  static async getHistoricalGlobal() {
    try {
      const historicalGlobalJson = await fetch(`${DISEASESH_BASE}/historical/all?lastdays=all`);
      const historicalGlobal = await historicalGlobalJson.json();
      return historicalGlobal;
    } catch (err) {
      throw new Error('Failed to load historicalGlobal');
    }
  }

  static async getHistoricalByCountry(countryCode, numberOfDays) {
    try {
      const historicalTotalCountryJson = await fetch(`${DISEASESH_BASE}/historical/${countryCode}?lastdays=${numberOfDays}`);
      const historicalTotalCountry = await historicalTotalCountryJson.json();
      return historicalTotalCountry;
    } catch (err) {
      throw new Error('Failed to load historicalTotalCountry');
    }
  }
}
